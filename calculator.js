function addition(firstNumber, secondNumber){
    const sum = firstNumber + secondNumber
    console.log(`The sum of ${firstNumber} and ${secondNumber} is ${sum}`)
}

function subtraction(firstNumber, secondNumber){
    const difference = firstNumber - secondNumber
    console.log(`The difference between ${firstNumber} and ${secondNumber} is ${difference}`);
}

function multiplication(firstNumber, secondNumber){
    const multiple = firstNumber * secondNumber
    console.log(`The multiplication of ${firstNumber} and ${secondNumber} is ${multiple}`);
}


function division(firstNumber, secondNumber){
    const division = firstNumber/ secondNumber 
    console.log(`The division of ${firstNumber} by ${secondNumber} is ${division}`);
}
    

addition(3,4)
subtraction(10,6)
multiplication(10,6)
division(10,2)
